		    <h1>Historique</h1>
			<p>Cette section présentera donc un historique de l'achèvement de mes pièces ainsi les résultats que j'ai obtenue en concours.<br/>
			Je n'ai nullement l'intention de jouer aux prétentieux avec cette section, mais ça permettra de suivre l'évolution chronologique de mes progrès.<br/>
			On vera comme ça la concordence des concours avec la création de nouvelles pièces. Puis ça me servira d'aide mémoire aussi ^^.</p>
			<p>J'ai commencé sérieusement les concours de figurines à partir du moment où je suis arrivé chez les "Canonniers". Avant j'ai juste participé une fois au "Golden Demon" en 2004 sans grand succès.<br/>
			Ce premier essai était plutôt l'occasion de participer à une telle manifestation qu'est le "Games Day".</p>
