			<h3>Liens incontournables :</h3>
			<dl>
			    <dt><a href="http://www.creafigs.com">Créafigs' :</a></dt>
				<dd>Site très intéressant pour sa grande communauté de hobbyistes de toutes parts et comprenant des membres de renommée internationale. Son forum très actif et ses tutoriaux très intéressants en font un site idéal pour aider peintres et sculpteurs, débutants comme confirmés. Attention, présentation obligatoire lors du premier post (simple formalité de politesse) ou les modos vous tomberont dessus ^^.</dd><br/>
				<dt><a href="http://www.coolminiornot.com">Coolminiornot :</a></dt>
				<dd>Site rassemblant des figurinistes du monde entier, mais qui a comme principale qualité, selon moi, de posséder une incroyable galerie de plusieurs milliers photos de figurines postées par les membres. Idéal quand on recherche de l'inspiration pour une pièce particulière.</dd><br/>
				<dt><a href="http://www.canonniers.org">Les Canonniers :</a></dt>
				<dd>C'est le site officiel des Canonniers. Il est assez récent donc n'est pas encore très riche, mais vous pourrez notamment y trouver quelques galeries des peintres membres.</dd>
			</dl><br/>
